//! Procedural macros for Nestix applications.
//!
//! Most users import these through `nestix` rather than depending on
//! `nestix-macros` directly. The macros here cover the three pieces of syntax
//! Nestix adds on top of regular Rust:
//!
//! - `#[component]` turns a function into a Nestix component type.
//! - `#[props]` turns a named-field struct into a prop container and builder.
//! - `layout!`, `callback!`, `computed!`, `destructure!`, and friends keep
//!   component bodies compact while still expanding to normal runtime APIs.
//!
//! ```ignore
//! use nestix::{Element, callback, component, create_state, layout};
//!
//! #[component]
//! fn Counter() -> Element {
//!     let count = create_state(0);
//!
//!     layout! {
//!         Button(
//!             .on_click = callback!([count] || count.mutate(|value| *value += 1)),
//!         ) {
//!             Text("Click")
//!         }
//!     }
//! }
//! ```

use proc_macro::TokenStream;

